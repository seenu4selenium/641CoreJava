package corejavatopics;

public class MethodDemo {

	//Add(): Method creation
	public static void add() {
		int a = 980;
		int b = 565;
		System.out.println(a + b);
	}
	public static void sub() {
		int a = 101;
		int b = 50;
		System.out.println(a - b);
	}


	public static void main(String[] args) {
		//Method calling
		add();
		sub();		
	}

}
